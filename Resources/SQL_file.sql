CREATE TABLE stroke_dataset (
    id INT PRIMARY KEY,
    gender VARCHAR(10),
    age FLOAT,
    hypertension BOOLEAN,
    heart_disease BOOLEAN,
    ever_married VARCHAR(5),
    work_type VARCHAR(20),
    residence_type VARCHAR(10),
    avg_glucose_level FLOAT,
    bmi FLOAT,
    smoking_status VARCHAR(20),
    stroke BOOLEAN
);

